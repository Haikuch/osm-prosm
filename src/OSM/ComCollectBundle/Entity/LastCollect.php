<?php

namespace OSM\ComCollectBundle\Entity;

/**
 * LastCollect
 */
class LastCollect
{
    /**
     * @var integer
     */
    private $id;

    /**
     * @var string
     */
    private $channel;

    /**
     * @var \DateTime
     */
    private $time;


    /**
     * Get id
     *
     * @return integer
     */
    public function getId()
    {
        return $this->id;
    }

    /**
     * Set channel
     *
     * @param string $channel
     *
     * @return LastCollect
     */
    public function setChannel($channel)
    {
        $this->channel = $channel;

        return $this;
    }

    /**
     * Get channel
     *
     * @return string
     */
    public function getChannel()
    {
        return $this->channel;
    }

    /**
     * Set time
     *
     * @param \DateTime $time
     *
     * @return LastCollect
     */
    public function setTime($time)
    {
        $this->time = $time;

        return $this;
    }

    /**
     * Get time
     *
     * @return \DateTime
     */
    public function getTime()
    {
        return $this->time;
    }
}
